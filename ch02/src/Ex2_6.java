
public class Ex2_6 {

	public static void main(String[] args) {
		int x = 100;
//		정수타입 변수 x에 100을 대입
		double pi = 3.14;
//		실수타입 변수 pi에 3.14를 대입
		char ch = 'a';
//		문자 1개를 저장할 변수의 타입을 char로 한다.
//		변수 ch에 문자 a를 대입
		String str = "abc";
//		문자 여러개를 저장할 변수의 타입을 String으로 한다.
//		변수 str에 문자열 abc를 대입
		
		System.out.println(x);
//		100
		System.out.println(pi);
//		3.14
		System.out.println(ch);
//		a
		System.out.println(str);
//		abc
	}

}
