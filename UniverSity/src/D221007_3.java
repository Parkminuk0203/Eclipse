import java.util.*;
public class D221007_3 {

	public static void main(String[] args) {
//		입력 받아 숫자 
		Scanner sc = new Scanner(System.in);
		
		System.out.println("반복할 횟수 입력: ");
		int i = sc.nextInt();
		
		while (i > 0) {
			System.out.println("환영합니다.");			
			i--;
		}
		
	} // end of main

} // end of class
